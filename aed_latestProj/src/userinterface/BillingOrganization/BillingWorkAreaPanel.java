    private void btnSendBillToConsumerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendBillToConsumerActionPerformed
        // TODO add your handling code here:
        UserAccount ua = (UserAccount) cmbConsumer.getSelectedItem();
        if (amount != 0.0) {
            BillValueToConsumer request = new BillValueToConsumer();
            request.setBillValue(amount);
            request.setConsumptionValue(consumption);
            ua.getWorkQueue().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null, "The consumer bill is sent");
        } else {
            JOptionPane.showMessageDialog(null, "You cannot charge 0 bill amount to Consumer");
        }

    }//GEN-LAST:event_btnSendBillToConsumerActionPerformed


