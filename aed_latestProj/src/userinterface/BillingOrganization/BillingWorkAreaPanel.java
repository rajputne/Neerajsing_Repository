    private void btnConsumptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsumptionActionPerformed
        // TODO add your handling code here:
        UserAccount ua = (UserAccount) cmbConsumer.getSelectedItem();
        if (enterprise instanceof GlobalEnterprise) {
            if (ua.getEmployee() instanceof Consumer) {
                Consumer consumer = (Consumer) ua.getEmployee();
                GlobalEnterprise ge = (GlobalEnterprise) enterprise;
                amount = ge.generateConsumerMonthlyBill(consumer);
                consumption = ge.calculateEnergyOfCurrentMonth(consumer.getSensor());
                lblEnergyConsumption.setText(String.valueOf(consumption));
                lblBillAmount.setText(String.valueOf(amount));
            }
        }
    }//GEN-LAST:event_btnConsumptionActionPerformed

