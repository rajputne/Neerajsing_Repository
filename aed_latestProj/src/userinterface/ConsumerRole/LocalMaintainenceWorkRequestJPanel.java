    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String message = txtMessage.getText();
        RequestToLocalMaintainence request = new RequestToLocalMaintainence();
        request.setSensor(consumer.getSensor());
        request.setSender(userAccount);
        request.setMessage(message);
        Organization org = null;
        for (Network n : business.getNetworkList()) {
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organiz : e.getOrganizationDirectory().getOrganizationList()) {
                    if (organiz instanceof LocalAreaEnergyMaintainenceOrganization) {
                        org = organiz;
                        org.getWorkQueue().getWorkRequestList().add(request);
                        JOptionPane.showMessageDialog(null, "Request For Maintainece submitted");
                    }
                }
            }
        }

        userAccount.getWorkQueue().getWorkRequestList().add(request);

    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnGetBackRepairedSensorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetBackRepairedSensorActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row.");
            return;
        }
        RequestToLocalMaintainence request = (RequestToLocalMaintainence) workRequestJTable.getValueAt(selectedRow, 0);
        consumer.setSensor(request.getSensor());
        JOptionPane.showMessageDialog(null, "The battery is fully charged");
    }//GEN-LAST:event_btnGetBackRepairedSensorActionPerformed

