    public void populateLocalConsumptionData(Sensor s) {
        DefaultTableModel model = (DefaultTableModel) localConsumptionTable.getModel();
        
        model.setRowCount(0);
        for (Map.Entry<Date, Integer> entry : s.getGlobalConsumptionList().entrySet()) {
            Object[] row = new Object[2];
            row[0] = entry.getKey();
            row[1] = entry.getValue();
            model.addRow(row);
        }
        
    }

