    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();
        
        if (selectedRow < 0) {
            return;
        }
        RequestToLocalMaintainence request = (RequestToLocalMaintainence) workRequestJTable.getValueAt(selectedRow, 0);
        ConsumerBattery conBattery = new ConsumerBattery();
        conBattery.setBatteryVolts(CommunitySensor.MAX_VOLTAGE);
        request.getSensor().setBattery(conBattery);
        JOptionPane.showMessageDialog(null, "Consumer Battery Set to Max Voltage!");
    }//GEN-LAST:event_btnSubmitActionPerformed
    

