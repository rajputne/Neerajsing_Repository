    private void populateCombo() {
        organizationJComboBox.removeAllItems();
        for (Organization.LocalOrganizationType type : Organization.LocalOrganizationType.values()) {
            if (!type.getValue().equals(LocalOrganizationType.Admin.getValue())) {
                organizationJComboBox.addItem(type);
            }
        }
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        for (Organization organization : directory.getOrganizationList()) {
            if (organization instanceof LocalAreaOrganization) {
                LocalAreaOrganization locOrg = (LocalAreaOrganization) organization;
                Object[] row = new Object[2];

                row[0] = organization.getName();
                row[1] = locOrg.getLocalAreaCode();
                model.addRow(row);
            }
        }
    }

