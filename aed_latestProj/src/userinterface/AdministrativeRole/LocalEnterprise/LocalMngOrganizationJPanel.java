    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        int code = 0;
        LocalOrganizationType type = (LocalOrganizationType) organizationJComboBox.getSelectedItem();
        MyIntegerVerifier intVerify = new MyIntegerVerifier();
        if (intVerify.verify(txtLocalOrgCode)) {
            //Now check for the Uniqueness of cade
            code = Integer.parseInt(txtLocalOrgCode.getText());
            for (Organization organization : directory.getOrganizationList()) {
                if (organization instanceof LocalAreaOrganization) {
                    if (((LocalAreaOrganization) organization).getLocalAreaCode() == code) {
                        isCodeMatch = true;
                        JOptionPane.showMessageDialog(null, "You cannot add same code for local area");
                    } else {
                        isCodeMatch = false;
                    }
                }
            }
            if (!isCodeMatch) {
                directory.createLocalOrganization(type, code);
                populateTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Some wrong data entered!");
        }
        //Check for Uniqueness of Code


    }//GEN-LAST:event_addJButtonActionPerformed


