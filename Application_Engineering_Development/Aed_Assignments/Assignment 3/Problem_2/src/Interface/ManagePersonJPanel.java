    public void populateTable() {
        //Make table Empty
        DefaultTableModel dtm = (DefaultTableModel) tblPerson.getModel();
        dtm.setRowCount(0);
        for (Person p : personDirectory.getPersonList()) {
            Object row[] = new Object[2];
            row[0] = p;
            row[1] = p.getAge();
            dtm.addRow(row);
        }
    }

