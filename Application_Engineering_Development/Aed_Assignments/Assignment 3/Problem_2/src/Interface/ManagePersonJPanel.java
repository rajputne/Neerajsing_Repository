    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String personName = txtSearch.getText();
        if (personName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cannot search empty person name", "Infomation", JOptionPane.INFORMATION_MESSAGE);
        } else {
            personName = txtSearch.getText();
            try {
                Person person = personDirectory.searchPerson(personName);
                SearchResultJPanel panel = new SearchResultJPanel(userProcessContainer, person);
                userProcessContainer.add("SearchResultJPanel", panel);
                CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
                //This helps layout manager pointing to the next layout
                cardLayout.next(userProcessContainer);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Cannot find the person name. Please try Again", "Infomation", JOptionPane.INFORMATION_MESSAGE);
                txtSearch.setText("");
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

