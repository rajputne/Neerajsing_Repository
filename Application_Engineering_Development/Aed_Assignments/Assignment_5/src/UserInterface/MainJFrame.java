    private void btnCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerActionPerformed
        if (!business.getCustomerDirectory().getCustomerList().isEmpty()) {
            ArrayList<String> getCustomerName = new ArrayList<String>();
            for (int i = 0; i < business.getCustomerDirectory().getCustomerList().size(); i++) {
                customer = business.getCustomerDirectory().getCustomerList().get(i);
                getCustomerName.add(customer.getCustomerName());
            }
            Object[] selectedStoreLocationOption = getCustomerName.toArray();
            String selectedStore = (String) JOptionPane.showInputDialog(null, "Please select the Customer Name for the Admin \n", "Options", JOptionPane.PLAIN_MESSAGE, null, selectedStoreLocationOption, "");
            if (selectedStore != null && !selectedStore.isEmpty()) {
                customer = business.getCustomerDirectory().searchCustomer(selectedStore);

                CustomerWorkAreaJPanel panel = new CustomerWorkAreaJPanel(userProcessContainer, business, customer);
                userProcessContainer.add("CustomerWorkAreaJPanel", panel);
                CardLayout cradlayout = (CardLayout) userProcessContainer.getLayout();
                cradlayout.next(userProcessContainer);
            } else {
                JOptionPane.showMessageDialog(null, "The store has to be selected to view the inventory");
            }
        } else {
            JOptionPane.showMessageDialog(null, "There are no stores!!!");
        }


    }//GEN-LAST:event_btnCustomerActionPerformed

