    public void populateOrderByCustomer() {
        DefaultTableModel dtm = (DefaultTableModel) orderTable.getModel();
        dtm.setRowCount(0);
        
        customer = (Customer) cmbCustomers.getSelectedItem();

        // oc=customer.getOrderCatalog();
        if (customer != null) {
            
            for (OrderItem orderItem : customer.getOrder().getOrderItemList()) {
                Object[] row = new Object[7];
                row[0] = orderItem;
                row[1] = orderItem.getSalesPrice();
                row[2] = orderItem.getQuantity();
                row[3] = orderItem.getTotalAmount();
                row[4] = orderItem.getProduct().getLowestPrice();
                row[5] = orderItem.getTargetPriceOfIndivual();
                row[6] = orderItem.getTargetPriceOfOverallItem();
                dtm.addRow(row);
            }
        }
    }

