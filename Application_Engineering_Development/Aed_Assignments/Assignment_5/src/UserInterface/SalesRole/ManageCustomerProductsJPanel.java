    private int parseInput(String inputNum) {
        try {
            int num = Integer.parseInt(inputNum);
            return num;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Format");
        }
        return 0;
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String inputNum = txtModifyPrice.getText();
        int selectedRow = orderTable.getSelectedRow();
        order = customer.getOrder();
        if ((!inputNum.isEmpty()) && selectedRow >= 0) {
            int newPrice = parseInput(inputNum);
            OrderItem orderItem = (OrderItem) orderTable.getValueAt(selectedRow, 0);
            updatePrice(orderItem, newPrice);
            populateComboBox();
            populateOrderByCustomer();
        }
        sales.setOrder(order);
    }//GEN-LAST:event_jButton2ActionPerformed

