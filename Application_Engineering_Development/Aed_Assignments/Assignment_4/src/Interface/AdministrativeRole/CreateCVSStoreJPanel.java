    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String storeLoc = txtStoreName.getText();

        boolean storeLocationAlreadyPresent = false;

        for (Store s : stores.getStoreList()) {
            if (storeLoc.equals(s.getStoreLocation())) {
                storeLocationAlreadyPresent = true;
            } else {
                storeLocationAlreadyPresent = false;
            }

        }
        if (storeLocationAlreadyPresent || storeLoc.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Store Location duplicate or empty");
        } else {
            Store store = stores.createStore();
            store.setStoreLocation(storeLoc);
            JOptionPane.showMessageDialog(null, "Store Location stored Successfully!!!");
            txtStoreName.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

